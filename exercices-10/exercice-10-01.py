# Créer une fonction nommée `my_sum()` qui :
# - prend deux paramètres
# - additionne ces deux paramètres
# - renvoie le résultat
# Appelez la fonction et affichez le résultat

# réponse 10.1

def my_sum(a, b):
    return a + b

resultat = my_sum(42, 123)
print(f"Le résultat est {resultat}")